## função que retorna se o número é para meu parceiro 

def epar(x):
    return x % 2 ==0
print(epar(2))
print(epar(3))

def maximo(x,y):
    if x > y:
        return x
    else:
        return y
print("qual é maior")   
print(maximo(2,6))
print(maximo(5,2))